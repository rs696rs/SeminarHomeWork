Console.Write("Введите число конца массива: ");
int maxValue = Convert.ToInt32(Console.ReadLine());

Console.WriteLine("");

int[,] my2dArray = CreatRandom2dArray(rows, columns, minValue, maxValue);

Show2dArray(my2dArray);

